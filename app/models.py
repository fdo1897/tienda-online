class Shoe(db.Model):
    __tablename__ = 'shoes'
    id = db.Column(db.Integer, primary_key=True)
    brand = db.Column(db.String(100))
    name = db.Column(db.String(100), nullable=False, unique=True)
    description = db.Column(db.String(1024))
    material = db.Column(db.String(100))
    price = db.Column(db.Integer, nullable=False)
    color = db.Column(db.String(50))
    size = db.Column(db.String(10), nullable=False)
    image = db.Column(db.String(255), nullable=False, unique=True)
    category_id = db.Column(db.Integer, db.ForeignKey('categories.id'))

    category = db.relationship('Category', backref='shoes')
    purchases = db.relationship('Purchase', back_populates='shoe')

    def buy(self, user):
        user.budget = int(user.budget)  # Convertir a entero si es una cadena
        self.owner = user.id
        user.budget -= self.price
        db.session.add(user)  # Agrega el usuario a la sesión
        db.session.commit()  # Guarda los cambios en la base de datos


    def __repr__(self):
        return '<Shoe %r>' % self.name


